    private void showMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_showMouseClicked
        String th = "";
        DefaultListModel<String> x = new DefaultListModel<>();
        A1.setModel(x);
        try {
            th = file.read_tesh();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(fram.class.getName()).log(Level.SEVERE, null, ex);
        }
        Scanner T = new Scanner(th);
        String word1 = "";
        while (T.hasNextLine()) {
            String Stu[] = T.nextLine().split(",");
            String Stu_1[] = Stu[0].split("=");
            word1 = Stu_1[1];
            x.addElement(word1);
        }

        A1.setFont(new Font("Serif", Font.BOLD, 17));
        String stu = "";
        DefaultListModel<String> t = new DefaultListModel<>();
        A2.setModel(t);
        try {
            stu = file.read_stu();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(fram.class.getName()).log(Level.SEVERE, null, ex);
        }

        Scanner S = new Scanner(stu);
        String word = "";
        while (S.hasNextLine()) {
            String Stu[] = S.nextLine().split(",");
            String Stu_1[] = Stu[0].split("=");
            word = Stu_1[1];
            t.addElement(word);
        }

        A2.setFont(new Font("Serif", Font.BOLD, 17));


    }//GEN-LAST:event_showMouseClicked

