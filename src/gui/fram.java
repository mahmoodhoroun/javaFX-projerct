    private void num_sKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_num_sKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {

            int h = 20;
            int num = Integer.parseInt(num_s.getText());

            for (int i = 0; i < num; i++) {
                JTextField x = new JTextField();
                jPanel2.add(x);
                jPanel2.setLayout(null);
                x.setBounds(20, h, 100, 30);
                x.setFont(new Font("Lucida Bright", 1, 14));
                S.add(x);
                x.setVisible(false);
                h += 35;
            }
            if (num > 0) {
                S.get(0).setVisible(true);
            }
            for (int i = 0; i < num; i++) {

                S.get(i).addKeyListener(new java.awt.event.KeyAdapter() {
                    public void keyPressed(java.awt.event.KeyEvent evt) {
                        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
                            try {
                                if (file.search_stu(S.get(Ts).getText())) {
                                    if (num - (Ts + 1) > 0) {
                                        S.get(Ts + 1).setVisible(true);
                                        Ts++;
                                    }
                                } else {

                                    JOptionPane.showMessageDialog(null, "try again");

                                }

                            } catch (FileNotFoundException ex) {
                                Logger.getLogger(fram.class.getName()).log(Level.SEVERE, null, ex);
                            }
                        }
                    }
                });
            }
           

        }
    }//GEN-LAST:event_num_sKeyPressed

